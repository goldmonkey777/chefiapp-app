// Handles login and signup

import React, { useState, useEffect } from 'react';
import { useAuth } from '../../hooks/useAuth';

interface OnboardingAuthProps {
  onComplete: (user: any) => void;
}

export const OnboardingAuth: React.FC<OnboardingAuthProps> = ({ onComplete }) => {
  const [mode, setMode] = useState<'login' | 'signup'>('signup');
  const [formData, setFormData] = useState({
    name: '',
    email: '',
    password: '',
    role: '',
  });
  const [loading, setLoading] = useState(false);
  const [error, setError] = useState<string | null>(null);

  const { signInWithGoogle, signInWithApple, signIn, signUp, user, isAuthenticated, isLoading: authLoading } = useAuth();

  // ‚úÖ CORRE√á√ÉO 1: Detectar quando user autenticar (via OAuth ou email)
  useEffect(() => {
    if (isAuthenticated && user && !authLoading) {
      console.log('üéØ [OnboardingAuth] User autenticado detectado:', {
        id: user.id,
        email: user.email,
        company_id: user.company_id
      });
      onComplete(user);
    }
  }, [isAuthenticated, user, authLoading, onComplete]);

  // ‚úÖ CORRE√á√ÉO 2: Implementar email/password auth
  const handleSubmit = async (e: React.FormEvent) => {
    e.preventDefault();
    setLoading(true);
    setError(null);

    try {
      if (mode === 'signup') {
        console.log('üìù [OnboardingAuth] Criando conta com email/password');
        await signUp(formData.email, formData.password, formData.name);
        // onComplete ser√° chamado automaticamente pelo useEffect acima
      } else {
        console.log('üîë [OnboardingAuth] Fazendo login com email/password');
        await signIn(formData.email, formData.password);
        // onComplete ser√° chamado automaticamente pelo useEffect acima
      }
    } catch (err: any) {
      console.error('‚ùå [OnboardingAuth] Erro:', err);
      setError(err.message || 'Erro ao autenticar');
      setLoading(false);
    }
  };

  return (
    <div className="min-h-screen bg-gradient-to-br from-blue-600 to-blue-800 flex items-center justify-center p-6 safe-area-insets">
      <div className="bg-white/10 backdrop-blur-md rounded-3xl p-8 max-w-md w-full border border-white/20">
        <div className="text-center mb-8">
          <div className="w-20 h-20 bg-white rounded-2xl mx-auto flex items-center justify-center mb-4 shadow-xl">
            <span className="text-4xl">üë®‚Äçüç≥</span>
          </div>
          <h2 className="text-3xl font-bold text-white mb-2">ChefIApp</h2>
          <p className="text-blue-100">
            {mode === 'signup' ? 'Criar conta' : 'Bem-vindo de volta'}
          </p>
        </div>

        {error && (
          <div className="bg-red-500/20 border border-red-400 text-red-100 p-3 rounded-xl mb-4 text-sm">
            {error}
          </div>
        )}

        {/* OAuth Buttons */}
        <div className="space-y-3 mb-6">
          <button
            onClick={async () => {
              setLoading(true);
              try {
                await signInWithGoogle();
              } catch (err: any) {
                setError(err.message);
              } finally {
                setLoading(false);
              }
            }}
            disabled={loading}
            className="w-full bg-white hover:bg-gray-50 text-gray-900 font-semibold py-3.5 rounded-xl shadow-lg transition flex items-center justify-center gap-3"
          >
            <svg className="w-5 h-5" viewBox="0 0 24 24">
              <path fill="#4285F4" d="M22.56 12.25c0-.78-.07-1.53-.2-2.25H12v4.26h5.92c-.26 1.37-1.04 2.53-2.21 3.31v2.77h3.57c2.08-1.92 3.28-4.74 3.28-8.09z" />
              <path fill="#34A853" d="M12 23c2.97 0 5.46-.98 7.28-2.66l-3.57-2.77c-.98.66-2.23 1.06-3.71 1.06-2.86 0-5.29-1.93-6.16-4.53H2.18v2.84C3.99 20.53 7.7 23 12 23z" />
              <path fill="#FBBC05" d="M5.84 14.09c-.22-.66-.35-1.36-.35-2.09s.13-1.43.35-2.09V7.07H2.18C1.43 8.55 1 10.22 1 12s.43 3.45 1.18 4.93l2.85-2.22.81-.62z" />
              <path fill="#EA4335" d="M12 5.38c1.62 0 3.06.56 4.21 1.64l3.15-3.15C17.45 2.09 14.97 1 12 1 7.7 1 3.99 3.47 2.18 7.07l3.66 2.84c.87-2.6 3.3-4.53 6.16-4.53z" />
            </svg>
            Continuar com Google
          </button>

          <button
            onClick={async () => {
              setLoading(true);
              try {
                await signInWithApple();
              } catch (err: any) {
                setError(err.message);
              } finally {
                setLoading(false);
              }
            }}
            disabled={loading}
            className="w-full bg-black hover:bg-gray-900 text-white font-semibold py-3.5 rounded-xl shadow-lg transition flex items-center justify-center gap-3"
          >
            <svg className="w-5 h-5" fill="currentColor" viewBox="0 0 24 24">
              <path d="M17.05 20.28c-.98.95-2.05.88-3.08.4-1.09-.5-2.08-.48-3.24 0-1.44.62-2.2.44-3.06-.4C2.79 15.25 3.51 7.59 9.05 7.31c1.35.07 2.29.74 3.08.8 1.18-.24 2.31-.93 3.57-.84 1.51.12 2.65.72 3.4 1.8-3.12 1.87-2.38 5.98.48 7.13-.57 1.5-1.31 2.99-2.54 4.09l.01-.01zM12.03 7.25c-.15-2.23 1.66-4.07 3.74-4.25.29 2.58-2.34 4.5-3.74 4.25z" />
            </svg>
            Continuar com Apple
          </button>
        </div>

        <div className="flex items-center gap-4 mb-6">
          <div className="flex-1 h-px bg-white/20"></div>
          <span className="text-sm text-blue-200">ou</span>
          <div className="flex-1 h-px bg-white/20"></div>
        </div>

        {/* Email/Password Form */}
        <form onSubmit={handleSubmit} className="space-y-4">
          {mode === 'signup' && (
            <>
              <input
                type="text"
                placeholder="Nome completo"
                value={formData.name}
                onChange={(e) => setFormData({ ...formData, name: e.target.value })}
                className="w-full bg-white/10 border border-white/30 rounded-xl px-4 py-3 text-white placeholder-blue-300/70 focus:outline-none focus:ring-2 focus:ring-white/50"
                required
              />
              <select
                value={formData.role}
                onChange={(e) => setFormData({ ...formData, role: e.target.value })}
                className="w-full bg-white/10 border border-white/30 rounded-xl px-4 py-3 text-white focus:outline-none focus:ring-2 focus:ring-white/50"
                required
              >
                <option value="" className="text-gray-900">Selecione seu cargo</option>
                <option value="reception" className="text-gray-900">Recep√ß√£o</option>
                <option value="kitchen" className="text-gray-900">Cozinha</option>
                <option value="service" className="text-gray-900">Bar/Servi√ßo</option>
                <option value="manager" className="text-gray-900">Gerente</option>
              </select>
            </>
          )}

          <input
            type="email"
            placeholder="Email"
            value={formData.email}
            onChange={(e) => setFormData({ ...formData, email: e.target.value })}
            className="w-full bg-white/10 border border-white/30 rounded-xl px-4 py-3 text-white placeholder-blue-300/70 focus:outline-none focus:ring-2 focus:ring-white/50"
            required
          />

          <input
            type="password"
            placeholder="Senha"
            value={formData.password}
            onChange={(e) => setFormData({ ...formData, password: e.target.value })}
            className="w-full bg-white/10 border border-white/30 rounded-xl px-4 py-3 text-white placeholder-blue-300/70 focus:outline-none focus:ring-2 focus:ring-white/50"
            required
          />

          <button
            type="submit"
            disabled={loading}
            className="w-full bg-white hover:bg-blue-50 text-blue-600 font-bold py-4 rounded-xl shadow-lg transition disabled:opacity-50"
          >
            {loading ? 'Carregando...' : mode === 'signup' ? 'Criar Conta' : 'Entrar'}
          </button>
        </form>

        <div className="mt-6 text-center">
          <button
            onClick={() => setMode(mode === 'signup' ? 'login' : 'signup')}
            className="text-sm text-blue-200 hover:text-white underline"
          >
            {mode === 'signup' ? 'J√° tem uma conta? Entrar' : 'N√£o tem conta? Criar'}
          </button>
        </div>
      </div>
    </div>
  );
};

export default OnboardingAuth;
